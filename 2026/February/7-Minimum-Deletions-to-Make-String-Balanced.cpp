#include <bits/stdc++.h>
using namespace std;

#define ll long long
#define all(vec) (vec).begin(), (vec).end()
#define sz(x) (int)(x).size()
#define i(x) (x - 'a')
#define updmax(a, x) (a = max((a), (x)))
#define updmin(a, x) (a = min((a), (x)))
#define nl '\n'

class Solution
{
public:
    int minimumDeletions(string s)
    {
        int dp = 0, cnt = 0;
        for (auto &c : s)
        {
            if (c == 'b')
                cnt++;
            else
                dp = min(dp + 1, cnt);
        }

        return dp;
    }
};